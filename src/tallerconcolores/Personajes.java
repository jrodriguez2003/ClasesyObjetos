package tallerconcolores;

/**
 *
 * @author l52
 */
public class Personajes {
    private String personaje;
    private String edad;
    private String tipo;
    private String quelegusta;

    /**
     * @return the personaje
     */
    public String getPersonaje() {
        return personaje;
    }

    /**
     * @param personaje the personaje to set
     */
    public void setPersonaje(String personaje) {
        this.personaje = personaje;
    }

    /**
     * @return the edad
     */
    public String getEdad() {
        return edad;
    }

    /**
     * @param edad the edad to set
     */
    public void setEdad(String edad) {
        this.edad = edad;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the quelegusta
     */
    public String getQuelegusta() {
        return quelegusta;
    }

    /**
     * @param quelegusta the quelegusta to set
     */
    public void setQuelegusta(String quelegusta) {
        this.quelegusta = quelegusta;
    }
}
